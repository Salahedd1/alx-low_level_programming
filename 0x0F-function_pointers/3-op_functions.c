#include "3-calc.h"

/**
 *op_add - add 2 int
 *@a: the 1st int
 *@b: the 2nd int
 *Return: addition of the 2 numbers
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 *op_sub - sub 2 int
 *@a: the 1st int
 *@b: the 2nd int
 *Return: subtitition of the 2 numbers
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - multiple 2 int
 *@a: the 1st int
 *@b: the 2nd int
 *Return: multiplication of the 2 numbers
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - div 2 int
 *@a: the 1st int
 *@b: the 2nd int
 *Return: div 2 numbers
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - reste of div of 2 int
 *@a: the 1st int
 *@b: the 2nd int
 *Return: module of the 2 numbers
*/

int op_mod(int a, int b)
{
	return (a % b);
}
